#include <iostream>
#include <cstdlib>

int main()
{
  std::cout << "Hello World!\n";
  //This is a single line comment

  /* this starts a multiple line comment
     we can just keep going with comments.
     everything contained by the slash-star and star-slash
     is treated like a comment. */
  std::cout << "We're out of the comments now!\n";
  // Another single line comment!
  return 0;
}
